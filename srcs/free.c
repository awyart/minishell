#include "header.h"

void ft_freechar2(char **cmd)
{
	int i;

	i = -1;
	while (cmd[++i] != NULL)
	{
		ft_strdel(&(cmd[i]));
	}
	if (cmd)
	{
		free(cmd);
		cmd = NULL;
	}
}