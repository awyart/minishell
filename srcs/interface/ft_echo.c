#include "header.h"

void	ft_echo(char **argv, char **env)
{
	int i;

	i = 1;
	(void)env;
	while (argv[i] != NULL)
	{
		ft_printf("%s ", argv[i]);
		i++;
	}
	ft_printf("\n");
}