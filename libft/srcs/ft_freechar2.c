#include "libft.h"

void	ft_freechar2(char **cmd)
{
	int i;

	i = -1;
	if (cmd && cmd[i + 1])
	{
		while (cmd[++i] != NULL)
		{
			ft_strdel(&(cmd[i]));
		}
		free(cmd);
		cmd = NULL;
	}
}
