#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t nb)
{
	size_t i;
	size_t k;

	i = ft_strlen(s1);
	k = 0;
	while (s2[k] && k < nb)
	{
		s1[i + k] = s2[k];
		k++;
	}
	s1[i + k] = '\0';
	return (s1);
}
